    def computeActionFromValues(self, state):
        """
          The policy is the best action in the given state
          according to the values currently stored in self.values.

          You may break ties any way you see fit.  Note that if
          there are no legal actions, which is the case at the
          terminal state, you should return None.
        """
        "*** YOUR CODE HERE ***"

        min_score = -90000 #Max infinity negative number
        get_possible_action = self.mdp.getPossibleActions(state)

        if len(get_possible_action) == 0:
          return None  

        best_action = None #initializing it to 0

        for action_cost in get_possible_action:
          q_value = self.computeQValueFromValues(state, action_cost)
          if (q_value >= min_score):
            min_score = q_value
            best_action = action_cost

